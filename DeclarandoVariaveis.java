public class DeclarandoVariaveis {

    String nome = "Adriana Borges Nogueira";
    int idade = 20;
    double altura = 1.70;
    boolean matricula = true;


    public static void main(String[] args) {
       DeclarandoVariaveis declarandoVariaveis = new DeclarandoVariaveis();
       System.out.println("Nome: " + declarandoVariaveis.nome);
       System.out.println("Idade: " + declarandoVariaveis.idade);
       System.out.println("Altura: " + declarandoVariaveis.altura);
       System.out.println("Matricula: " + declarandoVariaveis.matricula);

    }
}
